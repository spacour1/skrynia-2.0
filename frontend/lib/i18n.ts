const dictionaries: Record<Language, Dictionary> = { en, uk, ru };

type I18nState = {
  language: Language;
  hydrated: boolean;
  setLanguage: (language: Language) => void;
  setLanguageAndReload: (language: Language) => void;
  hydrate: () => void;
};

function applyLanguage(language: Language) {
  if (typeof document === "undefined") return;
  document.documentElement.lang = language;
}

function getInitialLanguage(): Language {
  if (typeof window === "undefined") return "ru";
  const saved = localStorage.getItem("language");
  if (saved === "en" || saved === "uk" || saved === "ru") return saved;
  const browserLanguage = navigator.language.toLowerCase();
  if (browserLanguage.startsWith("uk")) return "uk";
  if (browserLanguage.startsWith("en")) return "en";
  return "ru";
}

export const useLanguageStore = create<I18nState>((set) => ({
  language: "ru",
  hydrated: false,
  setLanguage: (language) => {
    localStorage.setItem("language", language);
    applyLanguage(language);
    set({ language, hydrated: true });
  },
  setLanguageAndReload: (language) => {
    localStorage.setItem("language", language);
    applyLanguage(language);
    set({ language, hydrated: true });
    window.location.reload();
  },
  hydrate: () => {
    const language = getInitialLanguage();
    applyLanguage(language);
    set({ language, hydrated: true });
  }
}));

export function translate(language: Language, key: string) {
  return dictionaries[language][key] ?? dictionaries.en[key] ?? key;
}

export function useI18n() {
  const { language, setLanguage, setLanguageAndReload, hydrate, hydrated } = useLanguageStore();

  useEffect(() => {
    if (!hydrated) hydrate();
  }, [hydrate, hydrated]);

  return {
    language,
    setLanguage,
    setLanguageAndReload,
    t: (key: string) => translate(language, key)
  };
}
